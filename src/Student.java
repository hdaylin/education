
public class Student { 
	
	private String status;  
	private String name;
	
	
	
	public void setStatus(String status){
		this.status = status;
	}
	
	
	public String getStatus(){
		return status; 
	} 
	
	
	public void setName(String name){
		this.name = name;
	}
	
	public String getName(){
		return name;
	} 
	
	
	
	
}
